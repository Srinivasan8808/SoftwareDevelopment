class Unary{
	public static void main(String[] args){
		int a=4;
		System.out.println(a++);
		System.out.println(a);
		
		int x=7;
		System.out.println(x--);
		System.out.println(x);
		System.out.println("******************");
		int p=13;
		System.out.println(p++);
		System.out.println(p++);
		System.out.println(p);
		
		System.out.println("******************");
		
		int q=16;
		System.out.println(q--);
		System.out.println(q--);
		System.out.println(q);
		
	}
}
		