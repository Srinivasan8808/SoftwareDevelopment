class Switch1{
	public static void main(String[] args)
	{
int option=1;
switch(option){
case 1:
{
	System.out.println("English");
}
break;
case 2:
{
	System.out.println("Tamil");
}
break;
case 3:
{
	System.out.println("Hindi");
}
break;
case 4:
{
	System.out.println("Urdu");
}

break;
default:
{
	System.out.println("INVALID Selection");
}
}
	}
}	