class Switch3{
	public static void main(String[] args){
		char value='A';
		
		switch(value){
			case 'A':
			System.out.println("Apple");
			break;
			case 'B':
			System.out.println("Ball");
			break;
			case 'C':
			System.out.println("cat");
			break;
			case 'D':
			System.out.println("Dog");
			break;
			default:
			System.out.println("Under development");
		}
	}
}